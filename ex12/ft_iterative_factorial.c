#include <unistd.h>

int	ft_iterative_factorial(int nb)
{
	int res;
	int i;

	if (nb == 0)
		return (1);
	else if (nb < 0 || nb > 12)
		return (0);
	i = 1;
	res = 1;
	while (i <= nb)
	{
		res = res * i;
		i++;
	}
	return (res);
}
